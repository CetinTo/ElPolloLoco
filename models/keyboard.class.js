/**
 * Represents a keyboard input handler
 * @class
 */

class Keyboard {
    /**
     * Left arrow key pressed state
     * @type {boolean}
     */
    LEFT = false;
    
    /**
     * Right arrow key pressed state
     * @type {boolean}
     */
    RIGHT = false;
    
    /**
     * Up arrow key pressed state
     * @type {boolean}
     */
    UP = false;
    
    /**
     * Down arrow key pressed state
     * @type {boolean}
     */
    DOWN = false;
    
    /**
     * Space key pressed state (jump action)
     * @type {boolean}
     */
    SPACE = false;
    
    /**
     * D key pressed state (throw action)
     * @type {boolean}
     */
    D = false;
}
